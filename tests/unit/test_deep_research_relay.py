# ─── Relay core behavior ─────────────────────────────────────────────────────

@pytest.mark.asyncio
async def test_truncation_continues_same_model(monkeypatch):
    """finish_reason=length → same model resumes; only that model is credited."""
    modelA = FakeModel(1, "modelA", "p1", "Model-A-405B")
    provider = ScriptedProvider({"modelA": [
        _content("PART1. ", "length"),   # truncated → keep going on A
        _content("PART2 done.", "stop"),  # natural stop → complete
    ]})
    _wire(monkeypatch, provider)
    content, handoffs, done = await _collect([modelA], provider)
    assert content == "PART1. PART2 done."
    assert handoffs == []                       # same model → no hand-off
    assert done["models_used"] == ["Model-A-405B"]
    assert provider.calls == ["modelA", "modelA"]


@pytest.mark.asyncio
async def test_rate_limit_hands_off_and_relays_accumulated_work(monkeypatch):
    """A truncates, then A rate-limits → B receives the work-so-far and finishes."""
    modelA = FakeModel(1, "modelA", "p1", "Model-A-405B")
    modelB = FakeModel(2, "modelB", "p2", "Model-B-671B")
    provider = ScriptedProvider({
        "modelA": [_content("PART1 from A. ", "length"),
                   RuntimeError("429 rate limit exceeded")],
        "modelB": [_content("PART2 from B, done.", "stop")],
    })
    _wire(monkeypatch, provider)
    content, handoffs, done = await _collect([modelA, modelB], provider)
    assert "PART1 from A." in content and "PART2 from B, done." in content
    assert handoffs == ["Model-B-671B"]
    assert done["models_used"] == ["Model-A-405B", "Model-B-671B"]


@pytest.mark.asyncio
async def test_stall_hands_off(monkeypatch):
    """A produces no token within the stall timeout → hand off to B."""
    monkeypatch.setattr(fr, "_DR_STALL_TIMEOUT_S", 0.1)
    modelA = FakeModel(1, "modelA", "p1", "Model-A-405B")
    modelB = FakeModel(2, "modelB", "p2", "Model-B-671B")
    provider = ScriptedProvider({
        "modelA": ["STALL"],
        "modelB": [_content("Recovered by B.", "stop")],
    })
    _wire(monkeypatch, provider)
    content, handoffs, done = await _collect([modelA, modelB], provider)
    assert content == "Recovered by B."
    assert done["models_used"] == ["Model-B-671B"]


@pytest.mark.asyncio
async def test_auth_failure_skips_whole_platform(monkeypatch):
    """A 401 on platform p1 → every p1 model is skipped; only p2's model runs."""
    modelA = FakeModel(1, "modelA", "p1", "Model-A")
    modelB = FakeModel(2, "modelB", "p1", "Model-B-samePlatform")
    modelC = FakeModel(3, "modelC", "p2", "Model-C")
    provider = ScriptedProvider({
        "modelA": [RuntimeError("401 unauthorized")],
        "modelC": [_content("From C.", "stop")],
    })
    _wire(monkeypatch, provider)
    content, handoffs, done = await _collect([modelA, modelB, modelC], provider)
    assert content == "From C."
    assert done["models_used"] == ["Model-C"]
    assert "modelB" not in provider.calls          # whole p1 platform skipped


