# ─── Fakes ───────────────────────────────────────────────────────────────────

class FakeModel:
    def __init__(self, id, model_id, platform, display_name):
        self.id = id
        self.model_id = model_id
        self.platform = platform
        self.display_name = display_name


class FakeKey:
    def __init__(self, id):
        self.id = id
        self.api_key = f"sk-fake-{id}"


class FakeQuery:
    def __init__(self, rows):
        self._rows = rows

    def filter(self, *a, **k):
        return self

    def all(self):
        return self._rows


class FakeDB:
    """Returns one fake key for every ApiKey query (platform filtering is a no-op
    here — model.platform gating is what the relay actually keys off)."""
    def query(self, *a, **k):
        return FakeQuery([FakeKey(10)])


class ScriptedProvider:
    """Yields events (or raises / stalls) per model_id, consuming one scripted
    action per call. Records the order of model_ids it was called with.

    An action is one of:
      - list[dict]            → the events to yield for that call
      - Exception instance    → raised
      - "STALL"               → sleeps longer than the (patched) stall timeout
    """
    def __init__(self, script):
        self.script = {k: list(v) for k, v in script.items()}
        self.calls = []

    async def stream_chat_completion_ex(self, api_key, messages, model_id,
                                        temperature=None, max_tokens=None):
        self.calls.append(model_id)
        actions = self.script.get(model_id)
        if not actions:
            yield {"type": "finish", "reason": "stop"}
            return
        action = actions.pop(0)
        if isinstance(action, Exception):
            raise action
        if action == "STALL":
            await asyncio.sleep(5)
            return
        for ev in action:
            yield ev


def _wire(monkeypatch, provider):
    """Patch the router's collaborators so the relay runs offline."""
    monkeypatch.setattr(fr.provider_registry, "get", lambda platform: provider)
    monkeypatch.setattr(fr, "_apply_owner_scope", lambda q: q)
    monkeypatch.setattr(fr.rate_limit, "is_on_cooldown", lambda *a, **k: False)
    monkeypatch.setattr(fr.rate_limit, "record_success", lambda *a, **k: None)
    monkeypatch.setattr(fr.rate_limit, "set_cooldown", lambda *a, **k: None)
    monkeypatch.setattr(fr.rate_limit, "record_rate_limit_hit", lambda *a, **k: None)
    monkeypatch.setattr(fr.rate_limit, "is_retryable_error", lambda e: True)


async def _collect(models, provider, messages=None):
    """Drive the relay to completion; return (content, handoffs, done_event)."""
    messages = messages or [MessageDto(role="user", content="build a project")]
    content, handoffs, done = "", [], None
    async for ev in _deep_research_relay(FakeDB(), models, messages, 0.7, 512):
        if ev["type"] == "content":
            content += ev["text"]
        elif ev["type"] == "handoff":
            handoffs.append(ev["model"])
        elif ev["type"] == "done":
            done = ev
    return content, handoffs, done


def _content(text, reason):
    return [{"type": "content", "text": text}, {"type": "finish", "reason": reason}]


