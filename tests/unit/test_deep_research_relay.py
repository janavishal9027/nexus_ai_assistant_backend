def _parity_script():
    return {
        "modelA": [_content("SEG_A. ", "length"),
                   RuntimeError("429 rate limit")],
        "modelB": [_content("SEG_B done.", "stop")],
    }


@pytest.mark.asyncio
async def test_streaming_and_nonstreaming_parity(monkeypatch):
    """Both entry points relay identically: same content + same models-used
    footer. Streaming additionally shows an inline hand-off marker."""
    modelA = FakeModel(1, "modelA", "p1", "Model-A-405B")
    modelB = FakeModel(2, "modelB", "p2", "Model-B-671B")
    pool = [modelA, modelB]
    monkeypatch.setattr(fr, "_get_ordered_models", lambda *a, **k: pool)
    msgs = [MessageDto(role="user", content="build a project")]

    # Non-streaming
    _wire(monkeypatch, ScriptedProvider(_parity_script()))
    non = await route_deep_research(FakeDB(), msgs)

    # Streaming (fresh provider — the script is stateful)
    _wire(monkeypatch, ScriptedProvider(_parity_script()))
    stream_res = await route_deep_research_stream(FakeDB(), msgs)
    streamed = ""
    async for chunk in stream_res.stream:
        streamed += chunk

    footer = "assembled across **2 models**: Model-A-405B → Model-B-671B"
    for text in (non.content, streamed):
        assert "SEG_A." in text and "SEG_B done." in text
        assert footer in text
    assert non.attempts == 2                     # two contributing models
    assert "Continuing with **Model-B-671B**" in streamed   # stream-only marker
    assert "Continuing with" not in non.content             # no marker non-stream
